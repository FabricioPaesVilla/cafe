    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        String n,e,c,d;
        n = txtNome.getText();
        e = txtEmail.getText();
        c = (String) cmbProblema.getSelectedItem().toString();
        d = txtDescricao.getText();
        
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            java.sql.Connection conectar = DriverManager.getConnection("jdbc:mysql://localhost:3306/cafe","root",""); 
            
            PreparedStatement st = conectar.prepareStatement("INSERT INTO ajuda VALUES(?,?,?,?)");
            st.setString(1,n);
            st.setString(2,e);
            st.setString(3,c);
            st.setString(4,d);
            st.executeUpdate();
            JOptionPane.showMessageDialog(null, "enviado com sucesso");
            txtNome.setText("");
            txtEmail.setText("");
            txtDescricao.setText("");
            
            
        } catch (ClassNotFoundException ex) {
            JOptionPane.showInternalMessageDialog(null, "entre em contato com o suporte e informe o erro:"+ ex.getMessage());
        } catch (SQLException ex) {
            String erro = ex.getMessage();
            if(erro.contains("Duplycate entry")){
                JOptionPane.showMessageDialog(null, "enviado");
            }else{
            JOptionPane.showInternalMessageDialog(null, "entre em contato com o suporte e informe o erro:"+ ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

